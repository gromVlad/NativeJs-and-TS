function memoize(f) {
  const cache = new Map(); // –ö–µ—à –∑–Ω–∞—á–µ–Ω–∏–π —Ö—Ä–∞–Ω–∏—Ç—Å—è –≤ –∑–∞–º—ã–∫–∞–Ω–∏–∏
  return function (...args) {
    // –°–æ–∑–¥–∞—Ç—å —Å—Ç—Ä–æ–∫–æ–≤—É—é –≤–µ—Ä—Å–∏—é –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è
    // –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∫–ª—é—á–∞ –∫–µ—à–∞
    let key = args.length + args.join("+");
    if (cache.has(key)) {
      return cache.get(key);
    } else {
      let result = f.apply(this, args);
      cache.set(key, result);
      return result;
    }
  }
}

